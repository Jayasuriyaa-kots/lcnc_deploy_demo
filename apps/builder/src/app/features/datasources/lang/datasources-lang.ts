export const formatDatasourcesMessage = (
  template: string,
  values: Record<string, string>
): string =>
  Object.entries(values).reduce(
    (next, [key, value]) => next.replace(new RegExp(`\\{${key}\\}`, 'g'), value),
    template
  );

type DatasourcesTranslateParams = Record<string, string | number | boolean>;

export const translateDatasourcesLang = (key: string, params?: DatasourcesTranslateParams): string => {
  const localKey = key.startsWith('datasources.') ? key.slice('datasources.'.length) : key;
  const value = localKey
    .split('.')
    .reduce<unknown>((current, segment) => {
      if (!current || typeof current !== 'object') {
        return undefined;
      }

      return (current as Record<string, unknown>)[segment];
    }, DATASOURCES_LANG);

  if (typeof value !== 'string') {
    return key;
  }

  if (!params) {
    return value;
  }

  return formatDatasourcesMessage(
    value,
    Object.fromEntries(Object.entries(params).map(([paramKey, paramValue]) => [paramKey, String(paramValue)]))
  );
};
